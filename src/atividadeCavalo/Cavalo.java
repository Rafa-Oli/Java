package atividadeCavalo;

public class Cavalo {
	private int linha, coluna;

	public Cavalo(int linha, int coluna) {
		this.linha = linha;
		this.coluna = coluna;
	}

	public void setLinha(int linha) {
		this.linha = linha;
	}

	public int getLinha() {
		return this.linha;
	}

	public void setColuna(int coluna) {
		this.coluna = coluna;
	}

	public int getColuna() {
		return this.coluna;
	}

	
	
}
